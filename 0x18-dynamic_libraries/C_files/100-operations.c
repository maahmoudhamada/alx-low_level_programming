#include "operations.h"

/**
* add - Function adds a and b
*
* @a: First num
* @b: Second num
*
* Return: Sum of two numbers
*/

int add(int a, int b)
{
return ((a) + (b));
}

/**
* sub - Function sub a and b
*
* @a: First num
* @b: Second num
*
* Return: Sub of two numbers
*/

int sub(int a, int b)
{
return ((a) - (b));
}

/**
* mul - Function mul a and b
*
* @a: First num
* @b: Second num
*
* Return: Mul of two numbers
*/

int mul(int a, int b)
{
return ((a) * (b));
}

/**
* div - Function div a and b
*
* @a: First num
* @b: Second num
*
* Return: Div of two numbers
*/

int div(int a, int b)
{
return ((a) / (b));
}

/**
* mod - Function mod a and b
*
* @a: First num
* @b: Second num
*
* Return: mod of two numbers
*/

int mod(int a, int b)
{
return ((a) % (b));
}

